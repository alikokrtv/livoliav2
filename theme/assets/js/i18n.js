function getLanguage() {
    return document.documentElement.lang || 'tr';
}

function t(key) {
    const lang = getLanguage();
    const keys = key.split('.');
    let result = translations[lang];
    if (!result) return key;
    
    for (const k of keys) {
        if (result[k]) {
            result = result[k];
        } else {
            return key;
        }
    }
    return result;
}

function initTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translated = t(key);
        
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.placeholder = translated;
        } else if (translated.includes('<br>') || translated.includes('<span')) {
             el.innerHTML = translated;
        } else {
            el.textContent = translated;
        }
    });

    const page = document.body.getAttribute('data-i18n-page');
    const currentLang = getLanguage();
    if (page && translations[currentLang].titles[page]) {
        document.title = translations[currentLang].titles[page] + " | LIVOLIA";
    }
}

document.addEventListener('DOMContentLoaded', initTranslations);
window.t = t;
